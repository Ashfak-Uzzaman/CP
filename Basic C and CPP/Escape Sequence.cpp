#include<iostream>
using namespace std;

int main()
{
    /* \t	Creates a horizontal tab   */
    cout<<"Hello world\t";
    cout<<"Programming is good\n";

/*    \\	Inserts a backslash character (\)                */
    cout<<"CSE is pera\\oooh man\n";

/* \"	Inserts a double quote character  */
    cout<<"\"CSE\" is good";

    return 0;
}
